namespace DesktopClock.Models;

/// <summary>
/// Enumerates the units of measurement for window alignment.
/// </summary>
public enum WindowAlignmentUnit
{
    /// <summary>
    /// Specifies that the window alignment is measured in pixels.
    /// </summary>
    Pixel,

    /// <summary>
    /// Specifies that the window alignment is measured in percent relative to the screen dimensions.
    /// </summary>
    Percent
}
